def assign_category_from_name(product_name):
    if pd.isna(product_name):
        return (None, None), "Помилка: порожня назва"

    text = str(product_name).lower()
    words = text.split()

    # Перевіряємо перші ДВА слова разом
    if len(words) >= 2:
        two_words = f"{words[0]} {words[1]}"
        if two_words in category_mapping:
            return category_mapping[two_words], None

    # Потім перше слово
    if words and words[0] in category_mapping:
        return category_mapping[words[0]], None

    # Fallback — пошук по всій назві
    for keyword, category in category_mapping.items():
        if keyword in text:
            return category, None

    return (None, None), f"НЕВИЗНАЧЕНО: {product_name}"


def extract_flavor(name):
    if pd.isna(name):
        return None
    text = str(name).lower()
    words = re.findall(r'[а-яіїєґa-z\']+', text)
    found = []
    for f in flavors:
        if f in words:
            found.append(flavor_normalize.get(f, f))
    found = list(dict.fromkeys(found))
    return ', '.join(found) if found else None


def extract_brand(name):
    """Шукає бренд зберігаючи оригінальний регістр."""
    if pd.isna(name):
        return None
    text = str(name)
    for brand in brands:
        if brand.lower() in text.lower():
            # Повертаємо бренд як він записаний в оригінальній назві
            idx = text.lower().find(brand.lower())
            return text[idx:idx + len(brand)]
    return None


def has_sales(row, file_type):
    if file_type == "pivot":
        return True
    elif file_type == "sales_no_vat":
        qty_col = "1 розница (кол-во)"
        sum_col = "1 розница (сумма без НДС)"
    elif file_type == "sales_with_vat":
        qty_col = "1 розница (кол-во)"
        sum_col = "1 розница (сумма с НДС)"
    else:
        return True
    if qty_col not in row or sum_col not in row:
        return True
    qty = row[qty_col]
    sum_val = row[sum_col]
    if pd.isna(qty) or pd.isna(sum_val):
        return False
    try:
        if float(qty) == 0 or float(sum_val) == 0:
            return False
    except (ValueError, TypeError):
        return False
    return True


