all_dataframes = []

print("Обробка файлів: \n")

for file_info in files_config:
    filename  = file_info["name"]
    file_type = file_info["type"]
    file_path = RAW_DIR / filename

    if not file_path.exists():
        print(f" Файл '{filename}' не знайдено.")
        continue

    try:
        engine = 'xlrd' if filename.endswith('.xls') else 'openpyxl'
        for sheet_name in file_info["sheets"]:
            try:
                header_row = 1 if filename == "1.xlsx" else 0
                df = pd.read_excel(
                    file_path, sheet_name=sheet_name,
                    engine=engine, header=header_row
                )
                col_match = next(
                    (c for c in df.columns if isinstance(c, str) and 'товар' in c.lower()),
                    None
                )
                if not col_match:
                    print(f" {filename} / {sheet_name} — колонку 'товар' не знайдено")
                    continue

                df.rename(columns={col_match: 'товар'}, inplace=True)
                df = df[df.apply(lambda row: has_sales(row, file_type), axis=1)]

                if len(df) == 0:
                    continue

                df['джерело_файл']  = filename
                df['джерело_аркуш'] = sheet_name
                all_dataframes.append(df)
                print(f" {filename} / {sheet_name}")

            except Exception as e:
                print(f"  {filename} / {sheet_name}: {e}")

    except Exception as e:
        print(f"   {filename}: {e}")

if not all_dataframes:
    print("\n Дані не завантажено.")
    exit()

