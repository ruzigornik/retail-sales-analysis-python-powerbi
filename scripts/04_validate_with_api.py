import pandas as pd
import anthropic
import json
import os
from pathlib import Path
from datetime import datetime

# API ключ береться зі змінної середовища.
# Перед запуском виконайте в терміналі:
#   export ANTHROPIC_API_KEY=your_key_here        (Mac / Linux)
#   set ANTHROPIC_API_KEY=your_key_here           (Windows CMD)
#   $env:ANTHROPIC_API_KEY="your_key_here"        (Windows PowerShell)
if not os.environ.get("ANTHROPIC_API_KEY"):
    raise EnvironmentError("ANTHROPIC_API_KEY не знайдено. Встановіть змінну середовища перед запуском.")


# ==========================================
# 1. НАЛАШТУВАННЯ
# ==========================================
BASE_DIR    = Path(__file__).parent.parent
REF_DIR     = BASE_DIR / "data" / "reference"
MASTER_FILE = REF_DIR / "directory_master.xlsx"
timestamp   = datetime.now().strftime("%Y%m%d_%H%M")
OUT_FILE    = REF_DIR / f"validation_report_{timestamp}.xlsx"

client = anthropic.Anthropic()

# ==========================================
# 2. СИСТЕМНИЙ ПРОМПТ
# ==========================================
SYSTEM_PROMPT = """Ти — експерт з категоризації снекової продукції українського ринку.
Перевір правильність даних для кожного товару.

ДЕРЕВО КАТЕГОРІЙ (використовуй тільки ці):
- Снеки → Картопляні чіпси, Фруктові чіпси, Тортилья чіпси, Картоп. палички, Кукурудзяні снеки, Попкорн, Хлібні снеки, Сирні снеки
- Горіхи та насіння → Горіхи, Насіння, Горіхові суміші
- Сухофрукти та цукати → Сухофрукти, Сушені фрукти, Ягоди, Цукати
- Рибні снеки → Сушена риба, Морепродукти, Рибні снеки
- М'ясні снеки → Джерки, М'ясні палички, М'ясні слайси
- Суміші → Снеки мікси, Горіхово-фруктові суміші, Пивні мікси
- Інше → Паніровка

ПРАВИЛА ПЕРЕВІРКИ:

Категорія і Підкатегорія:
- Перевір по назві товару чи логічно визначена категорія і підкатегорія
- Якщо правильно — пиши OK
- Якщо неправильно — пиши: Помилка: [пояснення] → [правильний варіант]

ТМ (торгова марка / бренд):
- Якщо ТМ порожня — перевір чи є бренд в назві товару
- Якщо бренд є в назві але ТМ порожня — пиши: Помилка: бренд не визначено → [бренд]
- Якщо ТМ заповнена правильно — пиши OK
- Якщо ТМ заповнена неправильно — пиши: Помилка: [пояснення] → [правильний варіант]

Смак:
- Якщо смак порожній — перевір чи потрібен він для цього товару:
  * Ціла риба без добавок (тарань, путасу, навага, кальмар тощо) — смак НЕ потрібен, пиши OK
  * Горіхи, насіння без добавок — смак НЕ потрібен, пиши OK
  * Сухофрукти, цукати — смак НЕ потрібен, пиши OK
  * Паніровка — смак НЕ потрібен, пиши OK
  * Якщо в назві є добавка (бекон, сир, васабі тощо) — смак потрібен, пиши: Помилка: смак не вказано → [запропонуй смак]
- Якщо смак заповнений — перевір чи відповідає назві товару
  * Якщо правильно — пиши OK
  * Якщо неправильно — пиши: Помилка: [пояснення] → [правильний варіант]

Коментар:
- Пиши тільки якщо є щось важливе що не вмістилось в колонки перевірки
- Якщо все OK і немає що додати — залишай порожнім

ВАЖЛИВО:
- Відповідай ТІЛЬКИ у форматі JSON масиву
- Без жодного тексту до або після JSON
- Для кожного товару обов'язково заповни всі 5 полів перевірки
- Навіть якщо ТМ або Смак порожні — все одно перевір і напиши OK або Помилка

Формат відповіді:
[
  {
    "товар": "назва товару як є",
    "Категорія_перевірка": "OK або Помилка: пояснення → пропозиція",
    "Підкатегорія_перевірка": "OK або Помилка: пояснення → пропозиція",
    "ТМ_перевірка": "OK або Помилка: пояснення → пропозиція",
    "Смак_перевірка": "OK або Помилка: пояснення → пропозиція",
    "Коментар": ""
  }
]"""

# ==========================================
# 3. ЧИТАЄМО ДОВІДНИК
# ==========================================
master_df = pd.read_excel(MASTER_FILE)
cols = ['товар', 'Категорія', 'Підкатегорія', 'ТМ', 'Смак', 'Вага']
master_df = master_df[cols].copy()

print(f"Завантажено товарів: {len(master_df)}")

# ==========================================
# 4. БАТЧЕВА ОБРОБКА
# ==========================================
BATCH_SIZE = 25
results = []

for i in range(0, len(master_df), BATCH_SIZE):
    batch = master_df.iloc[i:i + BATCH_SIZE]
    batch_num = i // BATCH_SIZE + 1
    total_batches = (len(master_df) + BATCH_SIZE - 1) // BATCH_SIZE
    print(f"Обробка батчу {batch_num}/{total_batches}...")

    # Формуємо список товарів для перевірки
    items = []
    for _, row in batch.iterrows():
        items.append({
            "товар": str(row['товар']),
            "Категорія": str(row['Категорія']) if pd.notna(row['Категорія']) else "",
            "Підкатегорія": str(row['Підкатегорія']) if pd.notna(row['Підкатегорія']) else "",
            "ТМ": str(row['ТМ']) if pd.notna(row['ТМ']) else "",
            "Смак": str(row['Смак']) if pd.notna(row['Смак']) else "",
        })

    user_prompt = f"""Перевір КОЖЕН товар зі списку. Для КОЖНОГО товару обов'язково поверни рядок в JSON навіть якщо поля порожні.

    {pd.DataFrame(items).to_string(index=False)}

    Кількість товарів: {len(items)}. У відповіді має бути рівно {len(items)} рядків."""

    try:
        response = client.messages.create(
            model="claude-haiku-4-5-20251001",
            max_tokens=4000,
            system=SYSTEM_PROMPT,
            messages=[{"role": "user", "content": user_prompt}]
        )

        text = response.content[0].text.strip()
        # Прибираємо можливі markdown бектіки
        text = text.replace("```json", "").replace("```", "").strip()
        batch_results = pd.DataFrame(json.loads(text))
        results.append(batch_results)
        print(f"  Батч {batch_num} оброблено: {len(batch_results)} товарів")

    except Exception as e:
        print(f"  Помилка батчу {batch_num}: {e}")
        # Додаємо порожні результати щоб не втратити товари
        empty = pd.DataFrame([{
            "товар": row['товар'],
            "Категорія_перевірка": "Помилка API",
            "Підкатегорія_перевірка": "Помилка API",
            "ТМ_перевірка": "Помилка API",
            "Смак_перевірка": "Помилка API",
            "Коментар": str(e)
        } for _, row in batch.iterrows()])
        results.append(empty)

# ==========================================
# 5. ЗБИРАЄМО РЕЗУЛЬТАТИ
# ==========================================
validation_df = pd.concat(results, ignore_index=True)

# Мерджимо з оригінальними даними по товару
final_df = master_df.merge(validation_df, on='товар', how='left')

# ==========================================
# 6. ЗБЕРЕЖЕННЯ
# ==========================================
final_df.to_excel(OUT_FILE, index=False)
print(f"\nГОТОВО!")
print(f"Файл: {OUT_FILE.name}")
print(f"Збережено в: {OUT_FILE.parent}")
print(f"Перевірено товарів: {len(final_df)}")

# Статистика помилок
for col in ['Категорія_перевірка', 'Підкатегорія_перевірка', 'ТМ_перевірка', 'Смак_перевірка']:
    errors = final_df[final_df[col].str.startswith('Помилка', na=False)][col].count()
    print(f"Помилок [{col}]: {errors}")